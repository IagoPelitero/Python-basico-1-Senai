num1 = int(input('Digite o primeiro número aqui: '))
num2 = int(input('Digite o segundo número aqui: '))

#soma
soma = num1 + num2
print("a soma de dois números é de: {0}".format(soma))

#subtração
sub = num1 - num2
print(f"a sub de dois números é de: {sub}")

#divisão
div = num1 / num2
print(f"a divisão de dois números é de: {div}")

#mult
mult = num1 * num2
print(f"a multiplicação de dois números é de: {mult}")

#exp
exp = num1 ** num2
print(f"a exponenciação de dois números é de: {exp}")

#divInteira
div_inteira = num1 // num2
print(f"a div inteira de dois números é de: {div_inteira}")

# mod
mod = num1 % num2
print(f"o módulo de dois números é de: {mod}")